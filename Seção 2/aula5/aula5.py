"""

+   Adição
-   Subtração
*   Multiplicação
/   Divisão
//  Divisão inteira aredondado
**  Exponenciação um numero elevado a outro
%   Modulo, resto da divisão
()  Altera precedencia nas contas

"""

print('Multiplicação * ', 10 * 10)
print('Adição + ', 10 + 10)
print('Subtração - ', 10 - 5)
print('Divisão / ', 10 / 2)
print('Multiplicação de string * ', 10 * '10')
print('Concatenação ', '10' + '10')
print(10.5 // 3)
print(2 ** 10)
print(10 % 3)  # Resto da divsão
print(5 + 2 * 10)
print((5 + 2) * 10)